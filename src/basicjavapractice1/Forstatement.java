package basicjavapractice1;

public class Forstatement {

    public static void main(String[] args){

        for(int a=0; a<=10; a++) {
            System.out.println(a);
        }
        for(int a=20; a>=15; a--){
            System.out.println(a);
        }
        int arr[] = {10,11,12,13,14,15};
        int length = arr.length;
        System.out.println(length);
    }
}
